package tastyboard;

public class TastyImage {
	private int tastyID;
	private String tiName;
	private String tiPath;
	
	public int getTastyID() {
		return tastyID;
	}
	public void setTastyID(int tastyID) {
		this.tastyID = tastyID;
	}
	
	public String getTiName() {
		return tiName;
	}
	public void setTiName(String tiName) {
		this.tiName = tiName;
	}
	
	public String getTiPath() {
		return tiPath;
	}
	public void setTiPath(String tiPath) {
		this.tiPath = tiPath;
	}
}
